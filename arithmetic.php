<?php

function add($a, $b)
{
    return $a + $b;
}

function subtract($a, $b)
{
    return $a - $b;
}

function multiply($a, $b)
{
    return $a * $b;
}

function divide($a, $b)
{
    return $a / $b;
}


echo add(6,4) .  PHP_EOL;
echo subtract(6,4) . PHP_EOL;
echo multiply(6,4) . PHP_EOL;
echo divide(6,3) . PHP_EOL;
